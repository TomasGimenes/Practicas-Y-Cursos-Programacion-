numero_entero = int(input("Dame un numero entero: "))

if numero_entero > 0:
    print("El numero que ingrasaste en positivo")

elif numero_entero == 0:
    print("El numero que ingrasaste es Igual a cero")

elif numero_entero < 0:
    print("El numero que ingresaste es negativo")

else:
    print("El dato ingresado no es un entero")