longitud = float(input("Dame la longitud del rectangulo: "))
ancho = float(input("Dame el ancho del rectangulo: "))

calcular_area = longitud * ancho
calcular_perimetro = 2 * (longitud + ancho)

print("el area del rectangulo es: ", calcular_area)
print("el perimetro del rectangulo es: ", calcular_perimetro)